<template>
  <div class="shopcart">
    <div class="content" @click="toggle()">
      <div class="content-left">
        <div class="logo-wrapper">
          <div class="logo" :class="{highlight:totalCount>0}">
            <i class="icon-shopping_cart" :class="{highlight:totalCount>0}"></i>
          </div>
          <div class="num" v-show="totalCount>0">{{totalCount}}</div>
        </div>
        <div class="price" :class="{highlight:totalCount>0}">￥{{totalPrice}}</div>
        <div class="desc">另需配送费￥{{deliveryPrice}}元</div>
      </div>
      <div class="content-right" @click.stop="pay">
        <div class="pay" :class="payClass">
          {{payDesc}}
        </div>
      </div>
    </div>
    <div class="ball-container">
      <div class="ball" transition="drop" v-for="ball in balls" v-show="ball.show">
        <div class="inner inner-hook"></div>
      </div>

    </div>

    <div class="shopcart-list" transition="fold" v-show="listShow">
      <div class="list-header">
        <h1 class="title">购物车</h1>
        <span class="empty" @click="clear">清空</span>
      </div>
      <div class="list-content" v-el:list-content>
        <ul>
          <li class="food" v-for="food in selectFoods">
            <span class="name">{{food.name}}</span>
            <div class="price">
              <span>￥{{food.price}}</span>
            </div>
            <div class="cartcontrol-wrapper">
              <cartcontrol :food="food"></cartcontrol>
            </div>
          </li>
        </ul>
      </div>
    </div>
  </div>
  <div class="list-mask" transition="fade" v-show="listShow" @click="toggle"></div>
</template>

<script>
  import Vue from 'vue'

  import BScroll from 'better-scroll'
  import cartcontrol from '../cartcontrol/cartcontrol'
  export default{
    props: {
      deliveryPrice: Number,
      minPrice:Number,
      selectFoods: Array
    },

    data () {
      return {
        show: false,
        balls: [
          {show: false},
          {show: false},
          {show: false},
          {show: false},
          {show: false}
        ]
      }
    },

    methods: {
      toggle () {
        if(this.totalCount===0) {
          return
        }
        this.show = !this.show
      },
      clear () {
        this.selectFoods.forEach(food => {
          food.count = 0
        })
      },
      pay() {
        if(this.totalPrice<this.minPrice) {
          return
        }
        alert(`支付${this.totalPrice}`)
      },

      //尽量找到一个球执行动画
      dropBall(el) {
        //从balls中找出一个隐藏的ball, 将其显示
        for (var i = 0; i < this.balls.length; i++) {
          var ball = this.balls[i];
          if(!ball.show) {
            console.log('使用ball', i);
            ball.show = true
            ball.el = el
            return
          }
        }
        console.log('没有可用的ball了');
      }
    },

    computed: {
      totalCount () {
        let count = 0
        this.selectFoods.forEach(food => {
          count += food.count
        })
        return count
      },
      totalPrice () {
        let price = 0
        this.selectFoods.forEach(food => {
          price += food.count*food.price
        })
        return price
      },
      payDesc () {
        const totalPrice = this.totalPrice
        const minPrice = this.minPrice
        if(totalPrice===0) {
          return `￥${minPrice}元起送`
        } else if(totalPrice<minPrice) {
          return `还差￥${minPrice-totalPrice}元起送`
        } else {
          return '去结算'
        }
      },
      payClass () {
        return this.totalPrice<this.minPrice ? 'not-enough' : 'enough'
      },
      listShow () {
        if(!this.totalCount) {
          this.show = false
          return false
        }

        if(this.show) {
          if(!this.scroll) { //不存在
            console.log('创建scroll');
            //创建scroll
            this.$nextTick(() => {
              this.scroll = new BScroll(this.$els.listContent, {
                click: true
              })
            })
          } else {
            //刷新scroll
            this.scroll.refresh()
          }
        }

        return this.show
      }
    },

    transitions: {
      drop: {
        //指定动画开始的位置
        beforeEnter (el) { //el当前要执行动画的球

          //从balls中找出最右边为true的ball
          let count = this.balls.length
          while(count--) {
            let ball = this.balls[count]
            if(ball.show) {
              ball.ballEl = el

              let rect = ball.el.getBoundingClientRect()
              const x = rect.left-32
              const y = -(window.innerHeight-rect.top-22)
              el.style.webkitTransform = `translate3d(0,${y}px,0)`
              el.style.transform = `translate3d(0,${y}px,0)`
              const innerEl = el.getElementsByClassName('inner-hook')[0]
              innerEl.style.webkitTransform = `translate3d(${x}px,0,0)`
              innerEl.style.transform = `translate3d(${x}px,0,0)`
              break
            }
          }
        },
        //指定动画结束的位置
        enter (el) {
          //导致重排重绘
          let rf = el.offsetHeight



          this.$nextTick(() => {
            el.style.webkitTransform = `translate3d(0,0,0)`
            el.style.transform = `translate3d(0,0,0)`
            const innerEl = el.getElementsByClassName('inner-hook')[0]
            innerEl.style.webkitTransform = `translate3d(0,0,0)`
            innerEl.style.transform = `translate3d(0,0,0)`
          })

        },
        //做一些收尾的工作
        afterEnter (el) { //动画结束
          console.log('afterEnter');
          //从balls中找出最左边show为true的ball, 将其改为false
          for (var i = 0; i < this.balls.length; i++) {
            var ball = this.balls[i];
            if(ball.show) {
              console.log('-------', i);
              ball.show = false
              ball.el = null
              // 手动隐藏
              ball.ballEl.style.display = 'none'

              // 有时动画结束后并不会调用afterEnter()
              break
            }
          }
        }
      }
    },

    components: {
      cartcontrol
    }
  }
</script>

